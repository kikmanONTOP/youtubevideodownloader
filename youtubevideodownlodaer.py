def download_video(url):
    try:
        yt = YouTube(url)
        video = yt.streams.get_highest_resolution()
        file_path = os.path.join(os.path.dirname(__file__), yt.title + ".mp4")
        video.download(output_path=os.path.dirname(__file__), filename=yt.title)
        print("the video was successfully downloaded")
        print("video path:", file_path)
    except Exception as e:
        print("error:", e)

def main():
    video_url = input("youtube video url: ")

    download_video(video_url)

if __name__ == "__main__":
    main()
